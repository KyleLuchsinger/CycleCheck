function getChecksum(str) {
    let result = [];
    
    // Handle empty string or single character
    if (str.length <= 1) {
      return result;
    }
    
    // Process each character and its subsequent character
    for (let i = 0; i < str.length; i++) {
      // If it's the last character, pair with the first character
      let nextIndex = (i === str.length - 1) ? 0 : i + 1;
      let pair = str[i] + str[nextIndex];
      result.push(primeSqrtTable[pair]);
    }
    return sumArray(result);
  }
  
  function cycleCheck(num1, num2){
    if(num1.length != num2.length) return false;
    let sum1 = getChecksum(num1.split(""));
    let sum2 = getChecksum(num2.split(""));
    console.log(num1 +" = ("+sum1+", "+num1.length+")");
    console.log(num2 +" = ("+sum2+", "+num2.length+")");
    if(sum1 != sum2) return false;
  
    return true;
  }
  
